package _02_method;

public class T02_api_method {
	// 4칙 연산 +,-,*,/
	
	/*	메소드4개
		return값 있음
		매개변수 2개
		
	
		
	*/	
	// +매개변수
	int add(int num1, int num2) {
		return num1+num2;
	}
	// -매개변수
	int subtract(int num1, int num2) {
		return num1-num2;
	}
	// *매개변수
	int multiply(int num1, int num2) {
		return num1*num2;
	}
	// /매개변수
	double divide(double num1, double num2) {
		return num1/num2;
	}
	
	
	
}
