package _02_StringMethod;

public class T07_equalsIgnoreCase {

	public static void main(String[] args) {
		// equals  vs equalsIgnoreCase
		
		String str = "hello";
			//equals : 대소문자를 가림
		System.out.println(str.equals("Hello")); //거짓
		
//		equalsIgnoreCase : 대소문자를 가리지 않음
		System.out.println(str.equalsIgnoreCase("Hello")); // 참
		
		String str2 = "Hello";
		
		
				
		
		
		
	}

}
