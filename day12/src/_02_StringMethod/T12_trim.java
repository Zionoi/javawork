package _02_StringMethod;

import java.util.Arrays;

public class T12_trim {

	public static void main(String[] args) {
		// trim : 문자열의 맨앞, 맨뒤 공백만 제거(중간에 있는 공백은 제거하지 않음)
		
		String str = "     리액트    프로그램     ";
		System.out.println(str+";");
		System.out.println(str.trim()+";");
		
		
		
		
	}

}
